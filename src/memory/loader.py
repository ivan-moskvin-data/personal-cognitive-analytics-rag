import logging
from pathlib import Path
from typing import Optional, List, Union

# Настройка логирования
logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")


class MemoryLoader:
    """
    Модуль загрузки оперативной памяти агента.
    Читает актуальный контекст из MEMORY.md и собирает системный промпт.
    """

    # Выносим базовые настройки в константы класса
    BASE_SYSTEM_PROMPT: str = "Ты — ИИ-ассистент PCAR. Отвечай дружелюбно, профессионально и по делу."
    MAX_FILE_SIZE_BYTES: int = 1024 * 1024  # Лимит в 1 МБ для защиты от OOM

    def __init__(self, memory_file_path: Optional[Union[Path, str]] = None) -> None:
        """Инициализация с поддержкой Dependency Injection (DI) для удобного тестирования."""
        if memory_file_path:
            self.memory_file: Path = Path(memory_file_path)
        else:
            base_dir = Path(__file__).resolve().parent.parent.parent
            self.memory_file = base_dir / "MEMORY.md"

    def get_current_memory(self) -> str:
        """Безопасно читает текущие задачи и состояние из MEMORY.md."""
        if not self.memory_file.is_file():
            logging.warning(f"[MEMORY] Файл {self.memory_file} не найден или это директория.")
            return ""

        # Защита от Out Of Memory: не читаем файл, если он случайно раздулся
        try:
            if self.memory_file.stat().st_size > self.MAX_FILE_SIZE_BYTES:
                logging.error(f"[MEMORY] Файл {self.memory_file} слишком большой (>{self.MAX_FILE_SIZE_BYTES} байт).")
                return ""

            content = self.memory_file.read_text(encoding="utf-8").strip()
            if content:
                return f"\n[ТЕКУЩИЙ КОНТЕКСТ И ЗАДАЧИ ИЗ MEMORY.MD]\n{content}"
        except (OSError, UnicodeDecodeError) as e:
            # Ловим только I/O и ошибки кодировки, а не абстрактный Exception
            logging.error(f"[MEMORY] Ошибка ввода/вывода или кодировки файла: {e}")

        return ""

    def build_system_prompt(self, retrieved_context: str) -> str:
        """Собирает финальный системный промпт (Инъекция памяти + HITL-инструкции)."""
        prompt = "Ты — ИИ-ассистент PCAR. Отвечай дружелюбно, профессионально и по делу.\n"
        
        current_memory = self.get_current_memory()
        if current_memory:
            prompt += current_memory
            
        if retrieved_context:
            prompt += f"\n[НАЙДЕННЫЕ ФАКТЫ ИЗ БАЗЫ ЗНАНИЙ (RAG)]\n{retrieved_context}\n"
            prompt += "\nИспользуй эти факты для ответа на вопрос пользователя, если они релевантны."

        # --- ПРОАКТИВНАЯ ИНСТРУКЦИЯ (РАСШИРЕННАЯ) ---
        prompt += """
\n[ВАЖНО: ПРОАКТИВНОЕ ОБНОВЛЕНИЕ БАЗЫ ЗНАНИЙ]
Ты функционируешь как автономный летописец. Твоя задача — непрерывно анализировать слова пользователя и выявлять факты, достойные сохранения в долгосрочную память (новые навыки, детали проектов, жизненные события, привычки, идеи, контакты).

ПРАВИЛА ИНИЦИАЦИИ ПАТЧА:
1. Если пользователь сообщил новую или уточняющую информацию — ты ДОЛЖЕН сгенерировать блок SEARCH/REPLACE в самом конце своего ответа.
2. Делай это молча. Не спрашивай «Хотите ли вы, чтобы я это записал?». Просто общайся, а блок с патчем прикрепи в самом низу.

ПРАВИЛА ВЫБОРА ФАЙЛА:
Ты имеешь доступ ко всей базе знаний. Логически подбирай файл для записи:
- `01_profile.md` — базовая информация, мировоззрение, глобальные факты о человеке.
- `02_projects.md` — прогресс по текущим задачам, новые идеи, архитектура пет-проектов.
- `03_tech_stack.md` — ИТ-инструменты, языки, фреймворки, изученные концепции.
- `04_health.md` — тренировки, питание, здоровье, сон.
- `05_strategy.md` — долгосрочные планы, карьерные или жизненные цели.
- `people.md` (или аналогичные) — новые знакомства, коллеги, их контакты.
- ЕСЛИ факт не подходит ни под одну категорию (например, лонгрид про путешествие или глубокий анализ финансов) — смело предлагай НОВЫЙ файл (например, `travel_2026.md` или `finance.md`).

ПРАВИЛА ОФОРМЛЕНИЯ ПАТЧА (КРИТИЧЕСКИ ВАЖНО):
1. Синтаксис Obsidian: ОБЯЗАТЕЛЬНО оборачивай все ключевые сущности (технологии, имена людей, названия проектов, концепции) в двойные квадратные скобки. Пример: `- Изучил [[Docker]] для проекта [[PCAR]]`.
2. Сохранение структуры: Уважай существующие списки (`- `) и заголовки (`### `). Не ломай форматирование.
3. Механика SEARCH:
   - Если нужно ИЗМЕНИТЬ факт: в блок SEARCH скопируй точную старую строчку из базы.
   - Если нужно ДОБАВИТЬ в конец файла: оставь блок SEARCH абсолютно пустым.
   - Если нужно ДОБАВИТЬ после определенного заголовка: в SEARCH укажи этот заголовок, а в REPLACE — заголовок и новую строку под ним.
4. Никаких блоков кода (```markdown) вокруг самого патча! Выводи его просто сырым текстом.

СТРОГИЙ ФОРМАТ ПАТЧА:
FILE: имя_файла.md
<<<<<<< SEARCH
(точный старый текст для поиска ИЛИ пустота)
=======
(новый текст с [[Obsidian]] ссылками и правильными отступами)
>>>>>>> REPLACE
"""
        return prompt